def _balance_tread_risers(
    tread_lines: list[StairTreadLine],
    total_rise_mm: float,
) -> list[float]:
    """Return a riser height per tread. Lines with riserHeightMm set are
    pinned; nulls receive an equal share of the remaining rise."""
    pinned_sum = sum(tl.riser_height_mm for tl in tread_lines if tl.riser_height_mm is not None)
    null_count = sum(1 for tl in tread_lines if tl.riser_height_mm is None)
    if pinned_sum > total_rise_mm + 1e-6:
        raise ValueError(
            f"pinned riser heights sum ({pinned_sum:.1f} mm) exceeds totalRiseMm ({total_rise_mm:.1f} mm)"
        )
    remaining = total_rise_mm - pinned_sum
    null_share = remaining / null_count if null_count > 0 else 0.0
    return [
        tl.riser_height_mm if tl.riser_height_mm is not None else null_share for tl in tread_lines
    ]


def _validate_stair_boundary(points: list[Vec2Mm]) -> None:
    """Raise ValueError for invalid stair boundary polygons."""
    if len(points) < 3:
        raise ValueError("stair boundary must have ≥ 3 points")
    n = len(points)
    # Self-intersection check runs before area so bowtie shapes get the right error.
    for i in range(n):
        ax, ay = points[i].x_mm, points[i].y_mm
        bx, by = points[(i + 1) % n].x_mm, points[(i + 1) % n].y_mm
        for j in range(i + 2, n):
            if i == 0 and j == n - 1:
                continue
            cx, cy = points[j].x_mm, points[j].y_mm
            dx, dy = points[(j + 1) % n].x_mm, points[(j + 1) % n].y_mm
            rx, ry = bx - ax, by - ay
            sx, sy = dx - cx, dy - cy
            denom = rx * sy - ry * sx
            if abs(denom) < 1e-7:
                continue
            qpx, qpy = cx - ax, cy - ay
            t = (qpx * sy - qpy * sx) / denom
            u = (qpx * ry - qpy * rx) / denom
            eps = 1e-6
            if (eps < t < 1.0 - eps) and (eps < u < 1.0 - eps):
                raise ValueError(f"stair boundary is self-intersecting (edge {i} crosses edge {j})")
    area2 = 0.0
    for i in range(n):
        a = points[i]
        b = points[(i + 1) % n]
        area2 += a.x_mm * b.y_mm - b.x_mm * a.y_mm
    if abs(area2) < 1.0:
        raise ValueError("stair boundary has zero area")


def _materialize_stair_runs_and_landings(
    cmd: CreateStairCmd,
) -> tuple[list[StairRun], list[StairLanding]]:
    """KRN-07: derive runs/landings from a CreateStairCmd.

    - Explicit runs[] passed by the caller win.
    - For shape='straight' with no runs, derive a single run from runStartMm/runEndMm.
    - For shape='l_shape'/'u_shape' with caller-supplied runs but no landings,
      auto-derive a rectangular landing per gap from adjacent run endpoints + width.
    """

    runs = list(cmd.runs)
    landings = list(cmd.landings)

    if not runs and cmd.shape == "straight":
        runs = [
            StairRun(
                id=f"{cmd.id or 'stair'}-run-1",
                start_mm=cmd.run_start_mm,
                end_mm=cmd.run_end_mm,
                width_mm=cmd.width_mm,
                riser_count=8,
            )
        ]

    if cmd.shape in ("l_shape", "u_shape") and runs and not landings:
        for i in range(len(runs) - 1):
            a = runs[i]
            b = runs[i + 1]
            landings.append(_auto_landing_for_gap(f"{cmd.id or 'stair'}-landing-{i + 1}", a, b))

    if cmd.shape == "spiral" and not runs:
        assert cmd.center_mm is not None
        assert cmd.inner_radius_mm is not None
        assert cmd.outer_radius_mm is not None
        assert cmd.total_rotation_deg is not None
        assert cmd.riser_count is not None and cmd.riser_count >= 1
        polyline = _spiral_polyline(
            cmd.center_mm,
            cmd.inner_radius_mm,
            cmd.outer_radius_mm,
            cmd.total_rotation_deg,
            cmd.riser_count,
        )
        runs = [
            StairRun(
                id=f"{cmd.id or 'stair'}-run-1",
                start_mm=polyline[0],
                end_mm=polyline[-1],
                width_mm=cmd.outer_radius_mm - cmd.inner_radius_mm,
                riser_count=cmd.riser_count,
                polyline_mm=polyline,
            )
        ]

    if cmd.shape == "sketch" and not runs:
        assert cmd.sketch_path_mm is not None and len(cmd.sketch_path_mm) >= 2
        polyline = list(cmd.sketch_path_mm)
        riser_count = cmd.riser_count if cmd.riser_count else max(len(polyline) - 1, 1)
        runs = [
            StairRun(
                id=f"{cmd.id or 'stair'}-run-1",
                start_mm=polyline[0],
                end_mm=polyline[-1],
                width_mm=cmd.width_mm,
                riser_count=riser_count,
                polyline_mm=polyline,
            )
        ]

    if cmd.authoring_mode == "by_sketch" and cmd.tread_lines:
        stair_id = cmd.id or "stair"
        runs = []
        for i in range(len(cmd.tread_lines) - 1):
            tl_a = cmd.tread_lines[i]
            tl_b = cmd.tread_lines[i + 1]
            mid_a = Vec2Mm(
                xMm=(tl_a.from_mm.x_mm + tl_a.to_mm.x_mm) / 2,
                yMm=(tl_a.from_mm.y_mm + tl_a.to_mm.y_mm) / 2,
            )
            mid_b = Vec2Mm(
                xMm=(tl_b.from_mm.x_mm + tl_b.to_mm.x_mm) / 2,
                yMm=(tl_b.from_mm.y_mm + tl_b.to_mm.y_mm) / 2,
            )
            runs.append(
                StairRun(
                    id=f"{stair_id}-run-{i + 1}",
                    start_mm=mid_a,
                    end_mm=mid_b,
                    width_mm=cmd.width_mm,
                    riser_count=1,
                )
            )

    return runs, landings


def _spiral_polyline(
    center: Vec2Mm,
    inner_r: float,
    outer_r: float,
    total_rot_deg: float,
    riser_count: int,
) -> list[Vec2Mm]:
    """Sample the spiral midline (between inner and outer radius) at riser_count+1 points."""

    import math

    midline_r = (inner_r + outer_r) * 0.5
    total_rot_rad = math.radians(total_rot_deg)
    out: list[Vec2Mm] = []
    for i in range(riser_count + 1):
        t = i / riser_count
        theta = total_rot_rad * t
        out.append(
            Vec2Mm(
                xMm=center.x_mm + midline_r * math.cos(theta),
                yMm=center.y_mm + midline_r * math.sin(theta),
            )
        )
    return out


def _auto_landing_for_gap(landing_id: str, a: StairRun, b: StairRun) -> StairLanding:
    """KRN-07: square landing centered on the join between runs `a` and `b`.

    Uses the larger of the two run widths so the landing always covers both treads.
    """

    cx = (a.end_mm.x_mm + b.start_mm.x_mm) * 0.5
    cy = (a.end_mm.y_mm + b.start_mm.y_mm) * 0.5
    half = max(a.width_mm, b.width_mm) * 0.5
    return StairLanding(
        id=landing_id,
        boundary_mm=[
            Vec2Mm(xMm=cx - half, yMm=cy - half),
            Vec2Mm(xMm=cx + half, yMm=cy - half),
            Vec2Mm(xMm=cx + half, yMm=cy + half),
            Vec2Mm(xMm=cx - half, yMm=cy + half),
        ],
    )


