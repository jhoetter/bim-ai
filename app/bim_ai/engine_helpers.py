def _dormer_footprint_polygon_mm(
    host: RoofElem,
    position: Any,
    width_mm: float,
    depth_mm: float,
) -> list[tuple[float, float]]:
    """KRN-14: four-corner CCW polygon of the dormer footprint in plan mm.

    Mirrors the renderer convention: width runs along the host roof's ridge
    axis (longer plan span), depth runs across.
    """

    xs = [p.x_mm for p in host.footprint_mm]
    ys = [p.y_mm for p in host.footprint_mm]
    cx = (min(xs) + max(xs)) / 2
    cy = (min(ys) + max(ys)) / 2
    span_x = max(xs) - min(xs)
    span_y = max(ys) - min(ys)
    ridge_along_x = span_x >= span_y
    half_w = width_mm / 2
    half_d = depth_mm / 2
    if ridge_along_x:
        centre_x = cx + position.along_ridge_mm
        centre_y = cy + position.across_ridge_mm
        min_x, max_x = centre_x - half_w, centre_x + half_w
        min_y, max_y = centre_y - half_d, centre_y + half_d
    else:
        centre_x = cx + position.across_ridge_mm
        centre_y = cy + position.along_ridge_mm
        min_x, max_x = centre_x - half_d, centre_x + half_d
        min_y, max_y = centre_y - half_w, centre_y + half_w
    return [
        (min_x, min_y),
        (max_x, min_y),
        (max_x, max_y),
        (min_x, max_y),
    ]


def _resolve_dormer_host_floor(
    els: dict[str, Element],
    host: RoofElem,
    fp_vertices: list[tuple[float, float]],
) -> FloorElem | None:
    """KRN-14: pick the floor on the host roof's reference level whose
    boundary contains the dormer footprint centre. Returns None when the
    reference level has no floor; falls back to the first candidate when
    the centre check is inconclusive."""

    candidates = [
        f
        for f in els.values()
        if isinstance(f, FloorElem) and f.level_id == host.reference_level_id
    ]
    if not candidates:
        return None
    centre_x = sum(v[0] for v in fp_vertices) / len(fp_vertices)
    centre_y = sum(v[1] for v in fp_vertices) / len(fp_vertices)
    for fl in candidates:
        poly = [(p.x_mm, p.y_mm) for p in fl.boundary_mm]
        if _point_in_polygon_xy(centre_x, centre_y, poly):
            return fl
    return candidates[0]


def _point_in_polygon_xy(px: float, py: float, poly: list[tuple[float, float]]) -> bool:
    n = len(poly)
    if n < 3:
        return False
    inside = False
    j = n - 1
    for i in range(n):
        xi, yi = poly[i]
        xj, yj = poly[j]
        if ((yi > py) != (yj > py)) and (px < (xj - xi) * (py - yi) / (yj - yi + 1e-12) + xi):
            inside = not inside
        j = i
    return inside


def _toposolid_elevation_at_centroid_mm(els: dict[str, Any], boundary: list[Any]) -> float | None:
    """TOP-V3-01: return toposolid surface elevation (mm) at the centroid of
    ``boundary`` if any toposolid's footprint contains that centroid, else None.

    Uses a simple average-of-samples interpolation for the flat/sparse case.
    """
    if not boundary:
        return None
    cx = sum(p.x_mm for p in boundary) / len(boundary)
    cy = sum(p.y_mm for p in boundary) / len(boundary)
    for el in els.values():
        if not isinstance(el, ToposolidElem):
            continue
        poly = [(p.x_mm, p.y_mm) for p in el.boundary_mm]
        if not _point_in_polygon_xy(cx, cy, poly):
            continue
        # Interpolate elevation: use weighted average of samples if available.
        from bim_ai.site.toposolid import samples_from_toposolid

        pts = samples_from_toposolid(el)
        if not pts:
            # Flat starter — use base_elevation_mm or 0.
            return el.base_elevation_mm or 0.0
        if len(pts) == 1:
            return pts[0][2]
        # Inverse-distance weighting (IDW) for sparse samples.
        total_weight = 0.0
        weighted_z = 0.0
        for sx, sy, sz in pts:
            d = math.hypot(cx - sx, cy - sy)
            if d < 1e-6:
                return sz
            w = 1.0 / d
            total_weight += w
            weighted_z += w * sz
        return weighted_z / total_weight if total_weight > 0 else 0.0
    return None


