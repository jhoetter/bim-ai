                if cand.get("levelId") != cmd.level_id:
                    continue
                bbox = cand.get("bboxMm") or {}
                mn = bbox.get("min") or {}
                mx = bbox.get("max") or {}
                x_lo = float(mn.get("x", 0))
                y_lo = float(mn.get("y", 0))
                x_hi = float(mx.get("x", 0))
                y_hi = float(mx.get("y", 0))
